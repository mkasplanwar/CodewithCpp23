#include <iostream>
using namespace std;

int main(){

    //inctrement (++) dan decrement (--)

    int x, y;

    //increment
    //post increment

    x = 1;
    cout<<x <<endl;
    x++;
    //x = x + 1;
    cout<<x<<endl;
    x++;
    cout<<x<<endl;
    cout<<"====================="<<endl;
    x = 1;
    cout<<x++<<endl;
    cout<<x++<<endl;
    cout<<x<<endl;//ini adalah x yang sekarang

    cout<<"====================="<<endl;

    //pre increment
    y = 1;
    cout<<y<<endl;
    ++y;
    //1 + y = y
    cout<<x<<endl;
    ++y;
    cout<<x<<endl;
    cout<<"======================"<<endl;

    y = 1;
    cout<<++y<<endl;
    cout<<++y<<endl;
    cout<<y<<endl;//y yang sekarang 

    //Decrement sebaliknya, prinsipnya sama


    
    

   
    
}